    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            Persona per;
            per = new Persona(Integer.parseInt(txtDNI.getText()), txtNombre.getText(), txtCurso.getText(), txtLibro.getText(), txtEntrega.getCalendar(), txtEntrega.getCalendar());
            if (ListaPersonas.addPersona(per)) {
                txtDNI.setText("");
                txtNombre.setText("");
                txtCurso.setText("");
                txtLibro.setText("");    
                txtEntrega.setCalendar(Calendar.getInstance());                
                txtDevolucion.setCalendar(Calendar.getInstance()); 
                
                JOptionPane.showMessageDialog(this, "Se guardo correctamente, el alumno tiene un mes limite para la devolucion.");
                ListaPersonas.saveLista();
            
            } else {
                JOptionPane.showMessageDialog(this, "No hay mas lugar");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "No cargo los datos");
            
        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

