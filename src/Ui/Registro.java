    private void refreshTable() {
        //Levantamos los datos a cargar
        Libro[] listado = ListaLibro.getLista();

        //Creamos el modelo de datos de la tabla.
        DefaultTableModel model = new DefaultTableModel();
       
        model.addColumn("");
        model.addColumn("Nombre");
        model.addColumn("Seccion");
        

        for (int i = 0; i < listado.length; i++) {
            if (listado[i] != null) {
                String[] fila = new String[3];
                fila[0] = "" + (i+1);
                fila[1] = listado[i].getNombre();
                fila[2] = listado[i].getSeccion();
                        
                model.addRow(fila);
            }
        }
        
        tblNaturales.setModel(model);
    
    }


